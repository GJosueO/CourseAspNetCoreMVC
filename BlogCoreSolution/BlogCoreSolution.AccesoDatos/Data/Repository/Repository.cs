        public void Remove(int id)
        {
           T entityToRemove = dbSet.Find(id); // OBTENER UNA ENTIDAD DE TIPO T POR ID, UTILIZANDO EL METODO FIND DEL CONJUNTO DE ENTIDADES DE TIPO T, Y ASIGNAR LA ENTIDAD OBTENIDA A LA VARIABLE ENTITYTOREMOVE, PARA PODER ELIMINAR LA ENTIDAD OBTENIDA DE LA BASE DE DATOS, UTILIZANDO EL METODO REMOVE DEL CONJUNTO DE ENTIDADES DE TIPO T, Y GUARDAR LOS CAMBIOS EN LA BASE DE DATOS.
        }

        public void Remove(T entity)
        {
            dbSet.Remove(entity);
        }

        // DE MANERA GENERAL, EL CONTROLADOR NO CONOCE DE dbcONTEXT,
        // Y ESTO ES DESACOMPLAMIENTO ,
        // EL CONTROLADOR SOLO CONOCE DE LA INTERFAZ IRepository,
        // Y ESTA INTERFAZ ES IMPLEMENTADA POR LA CLASE Repository,
        // QUE A SU VEZ UTILIZA EL CONTEXTO DE BASE DE DATOS PARA REALIZAR LAS OPERACIONES DE BASE DE DATOS.
        //
        // ESTO PERMITE QUE EL CONTROLADOR NO TENGA DEPENDENCIA DIRECTA DEL CONTEXTO DE BASE DE DATOS,
        // Y SE PUEDA CAMBIAR LA IMPLEMENTACION DE LA CLASE Repository SIN AFECTAR AL CONTROLADOR.

        // T QUIERE DECIR QUE ES UNA CLASE, Y SE PUEDE UTILIZAR PARA CUALQUIER CLASE QUE SE QUIERA AGREGAR A LA BASE DE DATOS, COMO CATEGORIA, ARTICULO, ETC. Y ENTITY ES EL OBJETO QUE SE QUIERE AGREGAR A LA BASE DE DATOS, Y SE AGREGA AL CONJUNTO DE ENTIDADES DE TIPO T, QUE ES EL CONJUNTO DE ENTIDADES QUE SE UTILIZA PARA REALIZAR LAS OPERACIONES DE BASE DE DATOS. Y ESTE METODO SE LLAMA DESDE EL CONTROLADOR, CUANDO SE QUIERE AGREGAR UNA NUEVA ENTIDAD A LA BASE DE DATOS. Y ESTE METODO SE ENCARGA DE AGREGAR LA ENTIDAD AL CONJUNTO DE ENTIDADES DE TIPO T, Y LUEGO SE GUARDA LOS CAMBIOS EN LA BASE DE DATOS:
        // ENTITY QUIERE DECIR QUE ES UN OBJETO DE TIPO T, QUE SE QUIERE AGREGAR A LA BASE DE DATOS, Y SE AGREGA AL CONJUNTO DE ENTIDADES DE TIPO T, QUE ES EL CONJUNTO DE ENTIDADES QUE SE UTILIZA PARA REALIZAR LAS OPERACIONES DE BASE DE DATOS. Y ESTE METODO SE LLAMA DESDE EL CONTROLADOR, CUANDO SE QUIERE AGREGAR UNA NUEVA ENTIDAD A LA BASE DE DATOS. Y ESTE METODO SE ENCARGA DE AGREGAR LA ENTIDAD AL CONJUNTO DE ENTIDADES DE TIPO T, Y LUEGO SE GUARDA LOS CAMBIOS EN LA BASE DE DATOS.
    }
}
