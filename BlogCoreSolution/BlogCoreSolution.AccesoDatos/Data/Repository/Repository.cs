namespace BlogCoreSolution.AccesoDatos.Data.Repository
{
    //DEBE ESTAR OPERACIONES COMUNES A TODAS LAS ENTIDADES, COMO AGREGAR, OBTENER, OBTENER TODOS, ELIMINAR, ETC.
    public class Repository<T> : IRepository<T> where T : class // ESTA CLASE SE ENCARGA DE HABLAR CON IDENTITY FRAMEWORK CORE PARA REALIZAR LAS OPERACIONES DE BASE DE DATOS, ESTA CLASE ES GENERICA, SE UTILIZARA PARA LA MAYORIA DEL PROYECTO, Y SE REEMPLAZARA CUANDO ENTRE MODELOS "CATEGORIA, ARTICULO"
    {
        protected readonly DbContext Context; // CONTEXTO DE BASE DE DATOS
        internal DbSet<T> dbSet; // CONJUNTO DE ENTIDADES DE TIPO T, SE UTILIZARA PARA REALIZAR OPERACIONES DE BASE DE DATOS
        public Repository(DbContext context) // CONSTRUCTOR QUE RECIBE EL CONTEXTO DE BASE DE DATOS
        {
            Context = context; // ASIGNAR EL CONTEXTO DE BASE DE DATOS A LA PROPIEDAD CONTEXT
            this.dbSet = context.Set<T>(); // OBTENER EL CONJUNTO DE ENTIDADES DE TIPO T DEL CONTEXTO DE BASE DE DATOS
        }
        public void Add(T entity) // METODO PARA AGREGAR UNA ENTIDAD DE TIPO T A LA BASE DE DATOS. T quiere decir que es una clase, y se puede utilizar para cualquier clase que se quiera agregar a la base de datos, como CATEGORIA, ARTICULO, ETC. y Entity es el objeto que se quiere agregar a la base de datos, y se agrega al conjunto de entidades de TIPO T, que es el conjunto de entidades que se utiliza para realizar las operaciones de base de datos. Y este metodo se llama desde el controlador, cuando se quiere agregar una nueva entidad a la base de datos. Y este metodo se encarga de agregar la entidad al conjunto de entidades de TIPO T, y luego se guarda los cambios en la base de datos.
        {
            dbSet.Add(entity); // AGREGAR LA ENTIDAD AL CONJUNTO DE ENTIDADES DE TIPO T
        }

        public T Get(int id)
        {
            return dbSet.Find(id); // OBTENER UNA ENTIDAD DE TIPO T POR ID, UTILIZANDO EL METODO FIND DEL CONJUNTO DE ENTIDADES DE TIPO T, Y RETORNAR LA ENTIDAD OBTENIDA
        }

        public IEnumerable<T> GetAll(Expression<Func<T, bool>> filter = null, Func<IQueryable<T>, IOrderedQueryable<T>> OrderBy = null, string? includeProperties = null)
        {
            //Se crea una consulta Iqueryable a partir de un DbSet del context.
            IQueryable<T> query = dbSet; // OBTENER EL CONJUNTO DE ENTIDADES DE TIPO T, Y ASIGNARLO A UNA VARIABLE DE TIPO IQueryable DE TIPO T, PARA PODER REALIZAR CONSULTAS LINQ SOBRE EL CONJUNTO DE ENTIDADES DE TIPO T, Y RETORNAR UNA COLECCION DE ENTIDADES DE TIPO T, QUE CUMPLAN CON LOS FILTROS, ORDENAMIENTOS E INCLUYENDO LAS PROPIEDADES RELACIONADAS QUE SE ESPECIFIQUEN EN LOS PARAMETROS DEL METODO.
            //Se aplica el filtro si se proporciona.
            if(filter != null)
            {
                query = query.Where(filter); // APLICAR EL FILTRO A LA CONSULTA, UTILIZANDO EL METODO WHERE DE LINQ, Y ASIGNAR LA CONSULTA FILTRADA A LA VARIABLE DE TIPO IQueryable DE TIPO T, PARA PODER REALIZAR MAS OPERACIONES SOBRE LA CONSULTA FILTRADA, Y RETORNAR UNA COLECCION DE ENTIDADES DE TIPO T, QUE CUMPLAN CON EL FILTRO ESPECIFICADO EN LOS PARAMETROS DEL METODO.
            }
            //Se incluyen las propiedades relacionadas si se especifican.
            if(includeProperties != null)
            {
                // SE DIVIDE LA CADENA DE PROPIEDADES POR COMA Y SE ITERA SOBRE ELLAS
                foreach(var property in includeProperties
                    .Split(new char[] { ','}, StringSplitOptions.RemoveEmptyEntries)) // DIVIDIR LA CADENA DE PROPIEDADES POR COMA, Y ELIMINAR LOS ELEMENTOS VACIOS, Y ASIGNAR CADA PROPIEDAD A LA VARIABLE PROPERTY, PARA PODER INCLUIR LAS PROPIEDADES RELACIONADAS EN LA CONSULTA, UTILIZANDO EL METODO INCLUDE DE LINQ, Y ASIGNAR LA CONSULTA INCLUIDA A LA VARIABLE DE TIPO IQueryable DE TIPO T, PARA PODER REALIZAR MAS OPERACIONES SOBRE LA CONSULTA INCLUIDA, Y RETORNAR UNA COLECCION DE ENTIDADES DE TIPO T, QUE CUMPLAN CON LOS FILTROS, ORDENAMIENTOS E INCLUYENDO LAS PROPIEDADES RELACIONADAS QUE SE ESPECIFIQUEN EN LOS PARAMETROS DEL METODO.
                {
                    query = query.Include(property);
                }
            }
            //Se aplica el ordenamiento si se proporciona.
            if (OrderBy != null)
            {
                return OrderBy(query).ToList(); // APLICAR EL ORDENAMIENTO A LA CONSULTA, UTILIZANDO EL DELEGADO FUNC DE ORDENAMIENTO, Y RETORNAR LA CONSULTA ORDENADA COMO UNA LISTA DE ENTIDADES DE TIPO T, QUE CUMPLAN CON LOS FILTROS, ORDENAMIENTOS E INCLUYENDO LAS PROPIEDADES RELACIONADAS QUE SE ESPECIFIQUEN EN LOS PARAMETROS DEL METODO.
            }
            //Si no se proporciona ordenamiento, simplemente se convierte la consulta en un lista.
            return query.ToList(); // CONVERTIR LA CONSULTA EN UNA LISTA DE ENTIDADES DE TIPO T, Y RETORNAR LA LISTA DE ENTIDADES DE TIPO T, QUE CUMPLAN CON LOS FILTROS, ORDENAMIENTOS E INCLUYENDO LAS PROPIEDADES RELACIONADAS QUE SE ESPECIFIQUEN EN LOS PARAMETROS DEL METODO.
        }

